/*
 * Criar uma calculadora básica.
 * 
 * O programa deve ter um método para cada tipo de operação da calculadora:
 * Soma
 * Subtração
 * Multiplicação
 * Divisão
 * 
 */

public class Exec01 {
	public static void main(String[] args) {
		

	}
	
	public int soma(int num1, int num2) {
		return num1 + num2;
	}
	
	public int subtracao(int num1, int num2) {
		return num1 - num2;
	}
	
	public int multiplicacao(int num1, int num2) {
		return num1 * num2;
	}
	
	public int divisao(int num1, int num2) {
		return num1 / num2;
	}
}