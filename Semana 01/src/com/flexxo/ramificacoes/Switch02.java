package com.flexxo.ramificacoes;

public class Switch02 {
	public static void main(String[] args) {
		int x = 10;

		switch (x) {
			case 10:
				System.out.println("10");
				break;
			case 11:
				System.out.println("11");
				break;
			default:
				System.out.println("default");
				break;
		}
	}
}
