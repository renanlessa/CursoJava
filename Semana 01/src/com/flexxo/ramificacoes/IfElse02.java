package com.flexxo.ramificacoes;

public class IfElse02 {
	public static void main(String[] args) {
		int maior = 10;
		int menor = 5;
		
		if (maior > menor)
			System.out.println(maior + ">" + menor);
		else 
			System.out.println(menor + ">" + maior);
		
		
		boolean vardade = (10 > 5);
		
		if(vardade)
			System.out.println(maior + ">" + menor);
		else 
			System.out.println(menor + ">" + maior);
	}
}
