package com.flexxo.ramificacoes;

public class Switch04 {
	public static void main(String[] args) {
		String x = "ghi";

		switch (x) {
			case "Abc":
				System.out.println("ABC");
				break;
			case "def":
				System.out.println("DEF");
				break;
			default:
				System.out.println("default");
				break;
			case "ghi":
				System.out.println("GHI");
				break;
		}
	}
}
